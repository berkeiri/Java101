public class AsalSayi {
    public static void main(String[] args) {
        int i;
        for (i=1;i<=100;i++){
            if (i%2==0){
                System.out.println(",");
            } else if (i%3==0) {
                System.out.println(",");
            }else {
                System.out.println(i+"asal");
            }

        }

    }
}
